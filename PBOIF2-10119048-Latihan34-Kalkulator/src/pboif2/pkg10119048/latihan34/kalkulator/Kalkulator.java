public class Kalkulator {
    double isi1,isi2;
    
    public double tambahBil() {
        return isi1 + isi2;
    }
    
    public double kurangBil() {
        return isi1 - isi2;
    }
    
    public double kaliBil() {
        return isi1 * isi2;
    }
    
    public double bagihBil() {
        return isi1 / isi2;
    }
    
    public double sisahBil() {
        return isi1 % isi2;
    }
}
